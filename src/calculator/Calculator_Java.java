    public void SwitchOper(){
        switch(oper){
            case "+":
                num =  num + Double.parseDouble(numS);
                break;
                    
            case "-":
                num =  num - Double.parseDouble(numS);
                break;
                    
            case "*":
                num =  num * Double.parseDouble(numS);
                break;
                    
            case "/":
                num =  num / Double.parseDouble(numS);
                break;
        }
        numS = String.valueOf(num);
        jTextField2.setText(numS);
    }

